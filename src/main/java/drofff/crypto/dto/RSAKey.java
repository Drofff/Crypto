package drofff.crypto.dto;

public class RSAKey {

	private Long exponent;

	private Long module;

	public Long getExponent() {
		return exponent;
	}

	public void setExponent(Long exponent) {
		this.exponent = exponent;
	}

	public Long getModule() {
		return module;
	}

	public void setModule(Long module) {
		this.module = module;
	}

}
